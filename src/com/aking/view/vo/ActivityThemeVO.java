package com.aking.view.vo;

public class ActivityThemeVO {
	private String id;
	private String activityThemeId;
	private String code;
	private String name;
	private String memo;

	public String getMemo() {
		return memo;
	}

	public void setMemo(String memo) {
		this.memo = memo;
	}

	public String getActivityThemeId() {
		return activityThemeId;
	}

	public void setActivityThemeId(String activityThemeId) {
		this.activityThemeId = activityThemeId;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
